#First Method
import math
print(math.sqrt(4))
print(int(math.sqrt(4)))       #--> TypeCasting from Float to Integer
print(math.pi)

#Second Method
import math as m
print(m.sqrt(4))
print(int(m.sqrt(4)))       #--> TypeCasting from Float to Integer
print(m.pi)

#Third Method
from math import *
print(sqrt(4))
print(int(sqrt(4)))       #--> TypeCasting from Float to Integer
print(pi)

